create table LEGAL_VERDICT
(
   articleId          int(11)   NOT NULL AUTO_INCREMENT COMMENT '裁判文书id',
   category           varchar(20)        DEFAULT NULL  COMMENT '文书类型',
   createTime         datetime           DEFAULT NULL  COMMENT '创建时间',
   creater            varchar(100)       DEFAULT NULL  COMMENT '创建人',
   lawCaseName        varchar(200)       DEFAULT NULL  COMMENT '案件名称',
   lawCaseTitle       varchar(500)       DEFAULT NULL  COMMENT '文书标题',
   lawCaseCode        varchar(100)       DEFAULT NULL  COMMENT '案件号',
   chiefJudge         varchar(100)       DEFAULT NULL  COMMENT '审判长',
   judge              varchar(100)       DEFAULT NULL  COMMENT '审判员',
   courtClerk         varchar(100)       DEFAULT NULL  COMMENT '书记员',
   courtName          varchar(100)       DEFAULT NULL  COMMENT '法院名称',
   accuser            varchar(500)       DEFAULT NULL  COMMENT '原告基本信息',
   accuserName        varchar(100)       DEFAULT NULL  COMMENT '原告姓名',
   accuserAgent       varchar(500)       DEFAULT NULL  COMMENT '原告代理人基本信息',
   accuserAgentName   varchar(100)       DEFAULT NULL  COMMENT '原告委托代理人姓名',
   defendant          varchar(500)       DEFAULT NULL  COMMENT '被告基本信息',
   defendantName      varchar(100)       DEFAULT NULL  COMMENT '被告姓名',
   defendantAgent     varchar(500)       DEFAULT NULL  COMMENT '被告代理人基本信息',
   defendantAgentName     varchar(100)       DEFAULT NULL  COMMENT '被告代理人姓名',
   causeOfAction      varchar(100)       DEFAULT NULL  COMMENT '案由',
   claims             varchar(1000)      DEFAULT NULL  COMMENT '诉讼请求',
   evidenceInfo       varchar(1000)      DEFAULT NULL  COMMENT '证据材料',
   courtCheckInfo     varchar(1000)      DEFAULT NULL  COMMENT '本院查明',
   verdict            varchar(1000)      DEFAULT NULL  COMMENT '裁判结论',
   legalBasis         varchar(256)       DEFAULT NULL  COMMENT '法律依据',
   acceptedDate       date               DEFAULT NULL  COMMENT '受理日期',
   judgmentDate       date               DEFAULT NULL  COMMENT '判决日期',
   deleteFlag         int(1)             DEFAULT 0  COMMENT '删除标记(0表示正常，1表示删除)',
   PRIMARY KEY (articleId)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='裁判文书属性表';
